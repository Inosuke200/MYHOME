jQuery(document).ready(function() { //premier menu-deroulant-survolant
            
    $('.helper').on('mouseover', function(e) {
        $('.helper-dropdown').show();
        
    });

    $('.helper-dropdown').on('mouseover', function(e) {
        $('.helper-dropdown').show();
        
    });

    $('.helper-dropdown').on('mouseout', function(e) {
        $('.helper-dropdown').hide();
        
    });

});


